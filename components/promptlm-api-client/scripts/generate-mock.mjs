/**
 * Generates the OpenAPI-driven mock backend surface under
 * `src/generated/mock/`:
 *
 *   - `operation-index.ts`   metadata for every operation (opId, method, path,
 *                            path-param order, matcher regex) so the runtime
 *                            can route an incoming request to a handler
 *                            without re-parsing the spec.
 *   - `example-responses.ts` schema-driven default response bodies for every
 *                            (opId, statusCode) pair, used by handlers that
 *                            do not synthesise a body from `MockBackendState`.
 *   - `handlers.ts`          one typed handler per operation, referencing
 *                            `operations[opId]['requestBody'/'responses']`
 *                            from `@promptlm/api-client`. Handlers delegate to
 *                            hand-written `MockBackendState` methods so the
 *                            generated surface only carries wire-format
 *                            knowledge; behaviour lives in `src/mock/`.
 *
 * Determinism is load-bearing: regenerating the files twice in a row must
 * produce zero `git diff`. We achieve that by:
 *   1. iterating operations in spec order (preserved by JSON parse → object
 *      property iteration order in Node ≥ 12 for non-integer keys);
 *   2. sorting all `Object.entries` we emit;
 *   3. resolving `$ref` synchronously with cycle detection that returns a
 *      placeholder rather than recursing forever.
 */

